/*
 * Обращение к индексу на примере строки.
 */
package ru.medwedSA.Java_Core_GB.Lectures.Lecture_1;

public class StringElementIndex {
    public static void main(String[] args) {
        String i = "gdhfjs";
        i.charAt(1); // обращение к индексу строки.
        System.out.println(i.charAt(1)); // d -> вывод значения первого индекса строки.
    }

}
